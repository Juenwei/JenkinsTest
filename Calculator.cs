namespace JenkinsTest
{
	public class Calculator
	{
		public int Add(int a, int b)
		{
			return a + b;
		}

		public int Multiply(int a, int b)
		{
			return a * b;
		}

		public int Square(int a)
		{
			return a * a;
		}

		public int Subtract(int a, int b)
		{
			return a - b;
		}

		public int Modulus(int a, int b)
		{
			return a % b;
		}
	}
}
